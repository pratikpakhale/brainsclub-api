const mongoose = require('mongoose')

const logSchema = new mongoose.Schema(
  {
    username: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
    type: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
)

const Log = mongoose.model('Log', logSchema)

module.exports = Log
